package io.tntra.java_designpatterns.creationalPattern.exwheelFactoryPattern;

public class carWheel extends wheelAbstrect{

  public carWheel(float diameter, float weight) {
    super(diameter, weight);
  }

  @Override
  public float getDiameter() {
    return super.getDiameter();
  }

  @Override
  public float getWeight() {
    return super.getWeight();
  }

  @Override
  public String toString() {
    return super.toString();
  }
}
